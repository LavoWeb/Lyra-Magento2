namespace Lyranetwork\Lyra\Model\Api\Ws;

class ExtInfo
{
    /**
     * @var string $key
     */
    private $key = null;

    /**
     * @var string $value
     */
    private $value = null;

    /**
     * @return string
     */
    public function getKey()
    {
        return $this->key;
    }

    /**
     * @param string $key
     * @return ExtInfo
     */
    public function setKey($key)
    {
        $this->key = $key;
        return $this;
    }

    /**
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }

    /**
     * @param string $value
     * @return ExtInfo
     */
    public function setValue($value)
    {
        $this->value = $value;
        return $this;
    }
}
